#recebimento do número
n = float(input("Escreva seu número: "))

#condicional se (if, elif, else)
if n < 10:
    print("Seu número é menor que 10")
elif n == 10:
    print("Seu número é igual a 10")
elif n > 10:
    print("Seu número é maior que 10")
else:
    print("Verifique os dados enviados")
